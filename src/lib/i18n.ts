const messages: Record<Locale, Record<MessageKey, string>> = { en, ar };

/** Translate a key for the given locale, with optional {placeholder} values. */
export function t(
  locale: Locale,
  key: MessageKey,
  values?: Record<string, string | number>
): string {
  let text = messages[locale][key] ?? en[key] ?? key;
  if (values) {
    for (const [name, value] of Object.entries(values)) {
      text = text.replace(`{${name}}`, String(value));
    }
  }
  return text;
}

/** Bind a locale once and reuse: const tr = getT(locale); tr("nav.home") */
export function getT(locale: Locale) {
  return (key: MessageKey, values?: Record<string, string | number>) =>
    t(locale, key, values);
}
